#include<stdio.h>

int main(){
    
    printf("ASCII Value of A : %d \n", 'A');
    printf("ASCII Value of Z : %d \n", 'Z');
    printf("ASCII Value of a : %d \n", 'a');
    printf("ASCII Value of z : %d \n", 'z');

    printf("\n");

    printf("Value of 65  : %c \n", 65);
    printf("Value of 90  : %c \n", 90);
    printf("Value of 97  : %c \n", 97);
    printf("Value of 122 : %c \n", 122);

    // 65 to 90  : A - Z    (capital)
    // 97 to 122 : a - z    (small)

    return 0;
}